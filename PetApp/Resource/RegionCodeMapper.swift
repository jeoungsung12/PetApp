            // 전라남도
        case (34.5107...35.4216, 126.2464...127.2174):
            return "6460000"
            
            // 경상북도
        case (35.7568...36.6544, 128.2457...129.4127):
            return "6470000"
            
            // 경상남도
        case (34.7568...35.4923, 127.7796...129.4127):
            return "6480000"
            
            // 제주특별자치도
